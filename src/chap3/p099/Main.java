package chap3.p099;

public class Main {
    public static void main(String[] args) {
        char c = 'p';

        if (c == 'a') {
            System.out.println("c の値は a です。");
        } else if (c == 'b') {
            System.out.println("c の値は b です。");
        } else {
            System.out.println("c の値は a でも b でもありません。");
            System.out.println("c の値は" + c + " です。");
        }
    }
}
